function Mostrar()
{
	var nota
	nota= Math.floor(Math.random()*(1,10))
	if (nota>=9)
	{
		
		alert("EXCELENTE")
	}
	else if(nota>=7)
	{
		alert ("BIEN PAPAA BIENNNN")
	}

	else if(nota>=4)
	{
		alert ("MAL AHI AMIGO")
	}

	else if(nota>=2)
	{
		alert ("BIEN...BIEN MAL")
	}

	else if (nota>=0)
	{
		alert ("JAJA PELOTUDO, DEDICATE A OTRA COSA")
	}
	//Genero el número RANDOM entre 1 y 10 
	

}//FIN DE LA FUNCIÓN